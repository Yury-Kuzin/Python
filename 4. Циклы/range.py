"""
    Функция для генерации последовательности
    Имеет 3 перегрузки:
    range(<number>) - генерирует последовательность от 0 до number (не включительно)
    range(<begin>, <end>) - генерирует последовательность от begin до end (не включительно)
    range(<begin>, <end>, <step>) - генерирует последовательность от begin до end (не включительно), с шагом step

"""

print(list(range(4, 50, 3)))
